
'''
第一题
解释： 元组Python中的不可变数据类型，创建了元组，不能对其修改，包括添加、删除或修改元素。
      元组的长度和元素都是固定的，这里直接创建一个新的元组a, 将原来的a和新元素放入，然后转成list即可。
'''
a = (7, 5, 3)
a = a + (10,)
b = list(a)
print(b)  #[7, 5, 3, 10]


