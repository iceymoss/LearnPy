c = [7, 5, 3, 10]
print(b is c)  #False 解释：is运算符用于检查两个对象是否相同，即它们是否指向相同的内存位置。在这种情况下，b 和 c 是两个不同的对象，内存地址不同。
print(b == c)  #True  解释: ==运算符用于检查两个对象的内容是否相同。在这里，b 和 c 包含相同的元素 [7, 5, 3, 10]

